/* Definição
Um array pode ser definido como uma lista ordenada de valores enumerados em que cada valor é chamado de elemento ou item, e cada elemento se localiza numa posição na lista chamada de índice.

Valores: se refere a dados de algum tipo: string (texto), number (número), booleano (true ou false) ou outros dados reconhecidos pelo JavaScript.
Lista ordenada: entende-se que os dados no array estão em uma ordem definida e se manterão nessa ordem.
Enumerados: cada dado no array está associado a um identificador numérico que diz qual é a sua posição na lista.
Cada valor é chamado de elemento ou item.
Cada elemento está localizado em uma posição na lista, chamada de índice
Em programação, chamamos de índice (em inglês, index) o número que identifica a posição de um valor em um array.
Para trabalhar com arrays com JavaScript, a contagem dos índices começa no número 0 e não no número 1.

// índice     0   1   2   3
const nums = [50, 43, 12, 98];

console.log(nums.length) // output: 4
".length" Esta propriedade especifica o tamanho de um array, ou melhor dizendo, o número de elementos. 

*/
